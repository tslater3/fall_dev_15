Duplicate a line
  Cmd-Shft-D

Indent a line Left or Right
  Cmd-] or Cmd-[

  Move an entire line up or down
    Cmd-Ctr-Up or Cmd-Ctrl-Down

  Move curson to the end of line
    Cmd-ArrowLeft or Cmd-ArrowRight

  Comment out line (regardless of filetype)
    Cmd-Slash(/)

  Enter a new line after current line
    Cmd-Enter

  Enter a new line before current line 
    Cmd-Shift-Enter

  Select text with arrow keys
    Hold Shift and use arrow keys

  Delete entire row where the cursor is
    Ctrl-Shft-k

  Multiple Cursors
    Hold Cmd and click in different spots

  Save as 
    Cmd-shift-s

  Search within file structure
    Cmd-p

  Search within file
    Cmd-f
